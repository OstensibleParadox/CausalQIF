"""
Import web-manual outputs into the standard 6.2 JSONL data layout.

Reads a packet generated by build_manual_web_packet.py and writes JSONL files
that are compatible with analyze.py and analyze_phase2a_ablation.py.

Usage:
  cd repo/
  python experiments/6.2_semantic_closure/import_manual_web_outputs.py \
      --packet_dir data/manual_web/bytedance_seed_diffusion/<run_id>
"""
import argparse
import csv
import json
from datetime import datetime, timezone
from pathlib import Path

REPO = Path(__file__).parents[2]


def _load_manifest(packet_dir):
    manifest = packet_dir / "manifest.csv"
    if not manifest.exists():
        raise RuntimeError(f"manifest not found: {manifest}")
    with open(manifest, newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def _read_output_text(path):
    if not path.exists():
        return ""
    txt = path.read_text(encoding="utf-8")
    # Strip helper comments from placeholder files.
    lines = [ln for ln in txt.splitlines() if not ln.strip().startswith("#")]
    return "\n".join(lines).strip()


def _build_record(row, prompt_text, output_text):
    ts = datetime.now(timezone.utc).isoformat()
    rec = {
        "model_name": row["target_jsonl"].split("/")[-2],
        "call_type": row["call_type"],
        "prompt": prompt_text,
        "generated_text": output_text,
        "logprobs": [],
        "timestamp": ts,
        "raw_response": {
            "backend": "manual_web",
            "source": "web_ui_manual_copy",
            "packet_job_id": row["job_id"],
            "phase": row["phase"],
            "arm": row["arm"],
        },
        "eval_count": 0,
        "done_reason": "stop",
        "finish_reason": "stop",
    }
    if row["phase"] == "phase1":
        rec["episode_id"] = row["episode_id"]
    else:
        rec["node_id"] = row["node_id"]
    return rec


def import_packet(packet_dir, allow_empty=False):
    rows = _load_manifest(packet_dir)
    if not rows:
        print("No rows in manifest.")
        return

    grouped = {}
    missing_outputs = 0
    imported_rows = 0

    for row in rows:
        prompt_path = packet_dir / row["prompt_file"]
        output_path = packet_dir / row["output_file"]
        target_jsonl = Path(row["target_jsonl"])

        prompt_text = prompt_path.read_text(encoding="utf-8")
        output_text = _read_output_text(output_path)

        if not output_text and not allow_empty:
            missing_outputs += 1
            continue

        rec = _build_record(row, prompt_text, output_text)
        grouped.setdefault(target_jsonl, []).append(rec)
        imported_rows += 1

    written = 0
    for path, records in grouped.items():
        # Keep deterministic call_type order.
        records = sorted(records, key=lambda r: 0 if r["call_type"] == "safety" else 1)
        path.parent.mkdir(parents=True, exist_ok=True)
        with open(path, "w", encoding="utf-8") as f:
            for r in records:
                f.write(json.dumps(r) + "\n")
        written += 1

    print("Manual outputs import complete:")
    print(f"  packet_dir:         {packet_dir}")
    print(f"  target jsonl files: {written}")
    print(f"  imported rows:      {imported_rows}")
    print(f"  missing outputs:    {missing_outputs}")
    if missing_outputs:
        print("  tip: rerun after filling outputs/*.txt or pass --allow_empty")


def main():
    p = argparse.ArgumentParser(description="Import manual web outputs into 6.2 JSONL layout")
    p.add_argument("--packet_dir", required=True, help="Packet directory produced by build_manual_web_packet.py")
    p.add_argument("--allow_empty", action="store_true", help="Import rows even when output text is empty")
    args = p.parse_args()

    import_packet(Path(args.packet_dir), allow_empty=args.allow_empty)


if __name__ == "__main__":
    main()
